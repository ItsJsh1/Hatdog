<script>
  const images = [
    "{{ asset('images/Events1.png') }}",
    "{{ asset('images/MainC3.png') }}",
    "{{ asset('images/Events2.png') }}",
    "{{ asset('images/Events3.png') }}"
  ];

  let currentIndex = 0;
  const carouselImage = document.getElementById('carousel-image');
  const dots = document.querySelectorAll('.carousel-dot');
  const prevButton = document.getElementById('carousel-prev');
  const nextButton = document.getElementById('carousel-next');

  function updateCarousel(index) {
    carouselImage.classList.add('opacity-0');
    setTimeout(() => {
      carouselImage.src = images[index];
      carouselImage.classList.remove('opacity-0');
    }, 300);

    dots.forEach((dot, i) => {
      dot.classList.toggle('bg-[#3F53E8]', i === index);
      dot.classList.toggle('bg-gray-300', i !== index);
    });
  }

  let autoSlideInterval = setInterval(() => {
    currentIndex = (currentIndex + 1) % images.length;
    updateCarousel(currentIndex);
  }, 3000);  

  prevButton.addEventListener('click', () => {
    currentIndex = (currentIndex - 1 + images.length) % images.length;
    updateCarousel(currentIndex);

    prevButton.classList.add('scale-90');
    setTimeout(() => prevButton.classList.remove('scale-90'), 150);

    resetAutoSlide();
  });

  nextButton.addEventListener('click', () => {
    currentIndex = (currentIndex + 1) % images.length;
    updateCarousel(currentIndex);

    nextButton.classList.add('scale-90');
    setTimeout(() => nextButton.classList.remove('scale-90'), 150);

    resetAutoSlide();
  });

  dots.forEach(dot => {
    dot.addEventListener('click', () => {
      currentIndex = parseInt(dot.dataset.index, 10);
      updateCarousel(currentIndex);

      resetAutoSlide();
    });
  });
  
  function resetAutoSlide() {
    clearInterval(autoSlideInterval);  
    autoSlideInterval = setInterval(() => {  
      currentIndex = (currentIndex + 1) % images.length;
      updateCarousel(currentIndex);
    }, 3000);  
  }

  updateCarousel(currentIndex);
</script>
